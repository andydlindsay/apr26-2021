# collections of key/value pairs
# JS objects, Python dictionaries, associative arrays, Ruby hash

user = {
  "username" => "jstamos",
  "password" => "1234"
}

puts user["username"]

user = {
  :username => 'jstamos',
  :password => '1234'
}

p user[:username]

user = {
  username: 'jstamos',
  password: '1234'
}

puts user
